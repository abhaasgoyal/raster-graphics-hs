module Model where

import CodeWorld

data Shape
  -- | The Line constructor holds the start and end coordinates of the line
  = Line Point Point
  -- | For the Polygon, a list of vertices
  | Polygon [Point]
  -- | Coordinates of opposite corners and angle of rotation
  | Rectangle Point Point Double
  -- | Centre point, then point on the circumference
  | Circle Point Point
  -- | Coordinates of the bounding box for the ellipse and angle of rotation
  | Ellipse Point Point Double
  -- | Coorordinates of opposite corners, then a third corner
  | Parallelogram Point Point Point
  deriving (Show)

type ColourShape = (ColourName, Shape)


data Tool
  = LineTool (Maybe Point)
  | PolygonTool [Point]
  | RectangleTool (Maybe Point)
  | CircleTool (Maybe Point)
  | EllipseTool (Maybe Point)
  | ParallelogramTool (Maybe Point) (Maybe Point)
  deriving (Eq, Show)

data ColourName
  = Black
  | Red
  | Orange
  | Yellow
  | Green
  | Blue
  | Purple
  deriving (Eq, Show, Enum)

data Model = Model [ColourShape] Tool ColourName
  deriving (Show)

-- | Starting Model for when CodeWorld first starts up.
emptyModel :: Model
emptyModel = Model [] (LineTool Nothing) Black

